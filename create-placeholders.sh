#!/bin/bash

# Create placeholder images for testing
cd public/photos

for model in 1 2 3 4 5; do
  for photo in 1 2 3; do
    filename="model${model}_${photo}.jpg"
    
    # Create a simple colored rectangle as placeholder using ImageMagick
    # If not available, create a simple text file as placeholder
    if command -v convert &> /dev/null; then
      convert -size 600x800 xc:"hsl($((model * 60)), 70%, 60%)" \
        -pointsize 60 -fill white -gravity center \
        -annotate +0+0 "Model ${model}\nPhoto ${photo}" \
        "$filename"
    else
      # Fallback: Create a minimal valid JPEG using printf
      # This creates a very small 1x1 red pixel JPEG
      printf '\xff\xd8\xff\xe0\x00\x10\x4a\x46\x49\x46\x00\x01\x01\x00\x00\x01\x00\x01\x00\x00\xff\xdb\x00\x43\x00\x08\x06\x06\x07\x06\x05\x08\x07\x07\x07\x09\x09\x08\x0a\x0c\x14\x0d\x0c\x0b\x0b\x0c\x19\x12\x13\x0f\x14\x1d\x1a\x1f\x1e\x1d\x1a\x1c\x1c\x20\x24\x2e\x27\x20\x22\x2c\x23\x1c\x1c\x28\x37\x29\x2c\x30\x31\x34\x34\x34\x1f\x27\x39\x3d\x38\x32\x3c\x2e\x33\x34\x32\xff\xc0\x00\x0b\x08\x00\x01\x00\x01\x01\x01\x11\x00\xff\xc4\x00\x14\x00\x01\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00\xff\xc4\x00\x14\x10\x01\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00\xff\xda\x00\x08\x01\x01\x00\x00\x3f\x00\xd2\xcf\x20\xff\xd9' > "$filename"
    fi
    
    echo "Created $filename"
  done
done

echo "Placeholder images created!"
